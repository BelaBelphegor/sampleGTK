#include <libft.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*s1;

	i = 0;
	s1 = (unsigned char *)s;
	while (i < n)
	{
		if (*s1 == (unsigned char)c)
			return ((void *)s1);
		s1++;
		i++;
	}
	return (NULL);
}
